    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        
        // Creamos el controlador JPA y obtenemos los datos desde la base de datos
        lista = pers.findTablapapeleriaEntities();
        
        // Mostramos los datos en la tabla
        DefaultTableModel modelo = (DefaultTableModel) tblListar.getModel();
        modelo.setRowCount(0);

        // Recorremos la lista y agregamos las filas a la tabla
        for (Tablapapeleria t : lista) {
            Object[] fila = {
                t.getId(),
                t.getCodigo(),
                t.getProducto(),
                t.getFamilia(),
                t.getPrecio()
            };
            modelo.addRow(fila);
        }
    }//GEN-LAST:event_btnListarActionPerformed

