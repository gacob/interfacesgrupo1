    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        // Obtenemos la ID
        int id=buscarID();
        
        // Si no existe, volvemos
        if (id == -1) {
            JOptionPane.showMessageDialog(this, "Este código no existe.");
            return;
        }
        
        // Buscamos los datos por ID y mostramos los datos
        Tablapapeleria t1 = pers.findTablapapeleria(id);
        txtProducto.setText(t1.getProducto());
        txtFamilia.setText(t1.getFamilia());
        txtCodigo.setText(Double.toString(t1.getPrecio()));
    }//GEN-LAST:event_btnBuscarActionPerformed

