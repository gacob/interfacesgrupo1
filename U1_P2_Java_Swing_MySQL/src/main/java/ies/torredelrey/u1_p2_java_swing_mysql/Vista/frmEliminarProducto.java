    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        // TODO add your handling code here:
        
        // Obtenemos la ID
        int id=buscarID();
        
        // Si no existe, volvemos
        if (id == -1) {
            JOptionPane.showMessageDialog(this, "Este código no existe.");
            return;
        }
        
        // Buscamos los datos por ID y lo borramos
        try {
            pers.destroy(id);
        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(this, "Este código no existe.");
            return;
        }
        
        // Volvemos a la pantalla de inicio
        this.dispose();
        frmMenuInicio nueva=new frmMenuInicio();
        nueva.setVisible(true);
    }//GEN-LAST:event_btnEliminarProductoActionPerformed
    
    private int buscarID() {
        int id=-1;
        
        // Guardamos el código
        String codigo=txtCodigo.getText();
        
        // Me guardo los datos de la base de datos
        List<Tablapapeleria> lista = pers.findTablapapeleriaEntities();
        
        // Conseguimos la ID
        for (Tablapapeleria t: lista) {
            if (t.getCodigo().equals(codigo)) {
                id=t.getId();
                break;
            }
        }
        
        return id;
    }
    
